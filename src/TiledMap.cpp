TiledMap::TiledMap()
{
    dirtTexture = TextureManager::LoadTexture("assets/map/dirt.png");
    grassTexture = TextureManager::LoadTexture("assets/map/grass.png");
    waterTexture = TextureManager::LoadTexture("assets/map/water.png");
    LoadMap(testMap);
    srcRect.x = srcRect.y = destRect.x = destRect.y = 0;
    srcRect.w = srcRect.h = destRect.w = destRect.h = 32;
}

TiledMap::~TiledMap()
{
    SDL_DestroyTexture(dirtTexture);
    SDL_DestroyTexture(grassTexture);
    SDL_DestroyTexture(waterTexture);
}

void TiledMap::LoadMap(int arr[20][25])
{
    for (int r = 0; r < 20; r++)
    {
        for (int c = 0; c < 25; c++)
        {
            map[r][c] = arr[r][c];
        }
    }
}

void TiledMap::DrawMap()
{
    int tileType = 0;
    for (int r = 0; r < 20; r++)
    {
        for (int c = 0; c < 25; c++)
        {
            tileType = map[r][c];
            destRect.x = c * destRect.w;
            destRect.y = r * destRect.h;

            switch (tileType)
            {
            case 0:
                // water type
                TextureManager::Draw(waterTexture, srcRect, destRect);
                break;
            case 1:
                // grass type
                TextureManager::Draw(grassTexture, srcRect, destRect);
                break;
            case 2:
                // dirt type
                TextureManager::Draw(dirtTexture, srcRect, destRect);
                break;
            default:
                break;
            }
        }
    }
}
